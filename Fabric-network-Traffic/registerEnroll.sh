#!/bin/bash

function createTrafficManagement() {
  echo "Enrolling the CA admin"
  mkdir -p organizations/peerOrganizations/trafficManagement.traffic.com/

  export FABRIC_CA_CLIENT_HOME=${PWD}/organizations/peerOrganizations/trafficManagement.traffic.com/

  set -x
  fabric-ca-client enroll -u https://admin:adminpw@localhost:7054 --caname ca-trafficManagement --tls.certfiles "${PWD}/organizations/fabric-ca/trafficManagement/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo 'NodeOUs:
  Enable: true
  ClientOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-trafficManagement.pem
    OrganizationalUnitIdentifier: client
  PeerOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-trafficManagement.pem
    OrganizationalUnitIdentifier: peer
  AdminOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-trafficManagement.pem
    OrganizationalUnitIdentifier: admin
  OrdererOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-trafficManagement.pem
    OrganizationalUnitIdentifier: orderer' > "${PWD}/organizations/peerOrganizations/trafficManagement.traffic.com/msp/config.yaml"

  # Since the CA serves as both the organization CA and TLS CA, copy the org's root cert that was generated by CA startup into the org level ca and tlsca directories

  # Copy trafficManagement's CA cert to trafficManagement's /msp/tlscacerts directory (for use in the channel MSP definition)
  mkdir -p "${PWD}/organizations/peerOrganizations/trafficManagement.traffic.com/msp/tlscacerts"
  cp "${PWD}/organizations/fabric-ca/trafficManagement/ca-cert.pem" "${PWD}/organizations/peerOrganizations/trafficManagement.traffic.com/msp/tlscacerts/ca.crt"

  # Copy trafficManagement's CA cert to trafficManagement's /tlsca directory (for use by clients)
  mkdir -p "${PWD}/organizations/peerOrganizations/trafficManagement.traffic.com/tlsca"
  cp "${PWD}/organizations/fabric-ca/trafficManagement/ca-cert.pem" "${PWD}/organizations/peerOrganizations/trafficManagement.traffic.com/tlsca/tlsca.trafficManagement.traffic.com-cert.pem"

  # Copy trafficManagement's CA cert to trafficManagement's /ca directory (for use by clients)
  mkdir -p "${PWD}/organizations/peerOrganizations/trafficManagement.traffic.com/ca"
  cp "${PWD}/organizations/fabric-ca/trafficManagement/ca-cert.pem" "${PWD}/organizations/peerOrganizations/trafficManagement.traffic.com/ca/ca.trafficManagement.traffic.com-cert.pem"

  echo "Registering peer0"
  set -x
  fabric-ca-client register --caname ca-trafficManagement --id.name peer0 --id.secret peer0pw --id.type peer --tls.certfiles "${PWD}/organizations/fabric-ca/trafficManagement/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo "Registering peer1"
  set -x
  fabric-ca-client register --caname ca-trafficManagement --id.name peer1 --id.secret peer1pw --id.type peer --tls.certfiles "${PWD}/organizations/fabric-ca/trafficManagement/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo "Registering user"
  set -x
  fabric-ca-client register --caname ca-trafficManagement --id.name user1 --id.secret user1pw --id.type client --tls.certfiles "${PWD}/organizations/fabric-ca/trafficManagement/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo "Registering the org admin"
  set -x
  fabric-ca-client register --caname ca-trafficManagement --id.name trafficManagementadmin --id.secret trafficManagementadminpw --id.type admin --tls.certfiles "${PWD}/organizations/fabric-ca/trafficManagement/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo "Generating the peer0 msp"
  set -x
  fabric-ca-client enroll -u https://peer0:peer0pw@localhost:7054 --caname ca-trafficManagement -M "${PWD}/organizations/peerOrganizations/trafficManagement.traffic.com/peers/peer0.trafficManagement.traffic.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/trafficManagement/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/trafficManagement.traffic.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/trafficManagement.traffic.com/peers/peer0.trafficManagement.traffic.com/msp/config.yaml"

  echo "Generating the peer0-tls certificates, use --csr.hosts to specify Subject Alternative Names"
  set -x
  fabric-ca-client enroll -u https://peer0:peer0pw@localhost:7054 --caname ca-trafficManagement -M "${PWD}/organizations/peerOrganizations/trafficManagement.traffic.com/peers/peer0.trafficManagement.traffic.com/tls" --enrollment.profile tls --csr.hosts peer0.trafficManagement.traffic.com --csr.hosts localhost --tls.certfiles "${PWD}/organizations/fabric-ca/trafficManagement/ca-cert.pem"
  { set +x; } 2>/dev/null

  # Copy the tls CA cert, server cert, server keystore to well known file names in the peer's tls directory that are referenced by peer startup config
  cp "${PWD}/organizations/peerOrganizations/trafficManagement.traffic.com/peers/peer0.trafficManagement.traffic.com/tls/tlscacerts/"* "${PWD}/organizations/peerOrganizations/trafficManagement.traffic.com/peers/peer0.trafficManagement.traffic.com/tls/ca.crt"
  cp "${PWD}/organizations/peerOrganizations/trafficManagement.traffic.com/peers/peer0.trafficManagement.traffic.com/tls/signcerts/"* "${PWD}/organizations/peerOrganizations/trafficManagement.traffic.com/peers/peer0.trafficManagement.traffic.com/tls/server.crt"
  cp "${PWD}/organizations/peerOrganizations/trafficManagement.traffic.com/peers/peer0.trafficManagement.traffic.com/tls/keystore/"* "${PWD}/organizations/peerOrganizations/trafficManagement.traffic.com/peers/peer0.trafficManagement.traffic.com/tls/server.key"

  echo "Generating the peer1 msp"
  set -x
  fabric-ca-client enroll -u https://peer1:peer1pw@localhost:7054 --caname ca-trafficManagement -M "${PWD}/organizations/peerOrganizations/trafficManagement.traffic.com/peers/peer1.trafficManagement.traffic.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/trafficManagement/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/trafficManagement.traffic.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/trafficManagement.traffic.com/peers/peer1.trafficManagement.traffic.com/msp/config.yaml"

  echo "Generating the peer1-tls certificates, use --csr.hosts to specify Subject Alternative Names"
  set -x
  fabric-ca-client enroll -u https://peer1:peer1pw@localhost:7054 --caname ca-trafficManagement -M "${PWD}/organizations/peerOrganizations/trafficManagement.traffic.com/peers/peer1.trafficManagement.traffic.com/tls" --enrollment.profile tls --csr.hosts peer1.trafficManagement.traffic.com --csr.hosts localhost --tls.certfiles "${PWD}/organizations/fabric-ca/trafficManagement/ca-cert.pem"
  { set +x; } 2>/dev/null

  # Copy the tls CA cert, server cert, server keystore to well known file names in the peer's tls directory that are referenced by peer startup config
  cp "${PWD}/organizations/peerOrganizations/trafficManagement.traffic.com/peers/peer1.trafficManagement.traffic.com/tls/tlscacerts/"* "${PWD}/organizations/peerOrganizations/trafficManagement.traffic.com/peers/peer1.trafficManagement.traffic.com/tls/ca.crt"
  cp "${PWD}/organizations/peerOrganizations/trafficManagement.traffic.com/peers/peer1.trafficManagement.traffic.com/tls/signcerts/"* "${PWD}/organizations/peerOrganizations/trafficManagement.traffic.com/peers/peer1.trafficManagement.traffic.com/tls/server.crt"
  cp "${PWD}/organizations/peerOrganizations/trafficManagement.traffic.com/peers/peer1.trafficManagement.traffic.com/tls/keystore/"* "${PWD}/organizations/peerOrganizations/trafficManagement.traffic.com/peers/peer1.trafficManagement.traffic.com/tls/server.key"

  echo "Generating the user msp"
  set -x
  fabric-ca-client enroll -u https://user1:user1pw@localhost:7054 --caname ca-trafficManagement -M "${PWD}/organizations/peerOrganizations/trafficManagement.traffic.com/users/User1@trafficManagement.traffic.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/trafficManagement/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/trafficManagement.traffic.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/trafficManagement.traffic.com/users/User1@trafficManagement.traffic.com/msp/config.yaml"

  echo "Generating the org admin msp"
  set -x
  fabric-ca-client enroll -u https://trafficManagementadmin:trafficManagementadminpw@localhost:7054 --caname ca-trafficManagement -M "${PWD}/organizations/peerOrganizations/trafficManagement.traffic.com/users/Admin@trafficManagement.traffic.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/trafficManagement/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/trafficManagement.traffic.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/trafficManagement.traffic.com/users/Admin@trafficManagement.traffic.com/msp/config.yaml"
}

